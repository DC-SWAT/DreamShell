static void __ask_60hz_fade16(unsigned short n)
{
	int i,j;
	unsigned short rs=0xB;
	unsigned short gs=0x5;
	unsigned short bs=0x0;
	unsigned short rm=0xF800;
	unsigned short gm=0x07E0;
	unsigned short bm=0x1F;
	unsigned short * buff=(unsigned short*)((unsigned)vram_l+(640-96/2)+(640*2*((480-60)/2)));
	for(j=0;j<60;j++)
	for(i=0;i<96;i++)
	{
		register unsigned short r=(__ask_60hz__[i+j*96]&rm)>>rs;
		register unsigned short g=(__ask_60hz__[i+j*96]&gm)>>gs;
		register unsigned short b=(__ask_60hz__[i+j*96]&bm)>>bs;
		if (r>n)
			r-=n;
		else
			r=0;
		if (g>n)
			g-=n;
		else
			g=0;
		if (b>n)
			b-=n;
		else
			b=0;
		buff[i+j*640]=((r<<rs) | (g<<gs) | (b<<bs));
	}
	timer_spin_sleep(10);
}

static int __ask_60hz_wait(void)
{
	unsigned i,p,u;
	unsigned char m=250;
	cont_state_t * cond = NULL;

	//DC: Updates KOS maple
	for(p=0;p<MAPLE_PORT_COUNT && m==250;p++){
		for(u=0;u<MAPLE_UNIT_COUNT && m==250;u++){
			//Get device
			maple_device_t *dev = maple_enum_dev(p,u);
			//Check type
			if (dev && dev->info.functions & MAPLE_FUNC_CONTROLLER) {
				cond = (cont_state_t *)maple_dev_status(dev);
				m=maple_addr(p,u);
			}
		}
	}
	for(i=0;i<200;i++)
	{
		if (cond && cond->buttons & CONT_Y) return !sdl_dc_default_60hz;
		timer_spin_sleep(10);
	}

	return sdl_dc_default_60hz;
}

static int sdl_dc_ask_60hz()
{
	static int ret=0x12345;
	int i;
	if (sdl_dc_no_ask_60hz)
		return sdl_dc_default_60hz;
	if (ret!=0x12345)
		return ret;
	for(i=128;i>=0;i--)
		__ask_60hz_fade16(i);

	ret=__ask_60hz_wait();

	for(i=0;i<=128;i++)
		__ask_60hz_fade16(i);
	return ret;
}
